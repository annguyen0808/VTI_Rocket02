package Assignment6;

public class Employee {

	protected String fullName;
	protected static float salaryMultiplier;
	protected static float salary;

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	protected float getSalaryMultiplier() {
		return salaryMultiplier;
	}

	protected void setSalaryMultiplier(float salaryMultiplier) {
		this.salaryMultiplier = salaryMultiplier;
	}

	
	protected static float getSalary() {
		return getSalary();
	}

	protected void setSalary(float salary) {
		this.salary = salary;
	}

	

}
